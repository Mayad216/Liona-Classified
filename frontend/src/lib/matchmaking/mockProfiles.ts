const u = (n: string) =>
  `https://images.unsplash.com/${n}?auto=format&fit=crop&w=400&q=80`;

function candidate(
  profile: Omit<RoommateProfile, "lookingFor"> & { roommatePreference?: string }
): RoommateProfile {
  const { roommatePreference = "Any", ...rest } = profile;
  return {
    ...rest,
    lookingFor: {
      gender_preference: roommatePreference,
    },
  };
}

