export function getGuestSeekerBase(): RoommateProfile {
  if (typeof window !== "undefined") {
    const params = new URLSearchParams(window.location.search);
    if (params.get("demo") === "male-seeker") {
      return demoMaleSeeker;
    }
    if (params.get("demo") === "female-layla") {
      return demoFemaleLaylaSeeker;
    }
  }
  return demoSeeker;
}
